#include<iostream>
using namespace std;
int main()
{
    int no1, no2;
    cout<<"Enter the First Number :"<<endl;
    cin>>no1;
    cout<<"Enter the Second Number :"<<endl;
    cin>>no2;
    // ADDITION
    cout<<no1<<" + "<<no2<<" = "<<no1+no2<<endl;
    // SUBTRACTION
    cout<<no1<<" - "<<no2<<" = "<<no1-no2<<endl;
    // MULTIPLICATION
    cout<<no1<<" x "<<no2<<" = "<<no1*no2<<endl;
    // DIVISION
    cout<<no1<<" / "<<no2<<" = "<<no1/no2<<endl;
    return 0;
}